export type RuKey = keyof typeof ru;

export function t(key: string, params?: Readonly<Record<string, string | number>>): string {
  const template = (ru as Record<string, string>)[key] ?? key;
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, k: string) => {
    const value = params[k];
    if (value === undefined) return `{${k}}`;
    const str = String(value);
    return str.startsWith('tile.') || str.startsWith('token.') || str.startsWith('card.')
      ? t(str)
      : str;
  });
}
